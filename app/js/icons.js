export function icon(name, { size = 20, className = "" } = {}) {
  const paths = PATHS[name];
  if (!paths) {
    return "";
  }

  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="icon ${className}" aria-hidden="true">${paths}</svg>`;
}

export function enhanceIconElements(root = document) {
  root.querySelectorAll("[data-icon]").forEach((element) => {
    const name = element.dataset.icon;
    const size = Number.parseInt(element.dataset.iconSize || "20", 10);
    element.innerHTML = icon(name, { size, className: element.dataset.iconClass || "" });
  });
}

